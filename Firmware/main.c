void SendKey(BYTE Code, BYTE Modifiers)
{
	Endpoint_3.CStatus = 0;
	while (Endpoint_3.CStatus & 0x40);

	Endpoint_3.FIFORegister = Modifiers;
	Endpoint_3.FIFORegister = 0;
	Endpoint_3.FIFORegister = Code;

	for (int i = 0; i < 5; i++)
	{
		Endpoint_3.FIFORegister = 0;
	}

	Endpoint_3.LengthL = 8;
	Endpoint_3.LengthM = 0;
	Endpoint_3.LengthH = 0;
	Endpoint_3.CStatus = 0x40;
}

void main()
{
	InitHardware();     // Initialize hardware
	DoUSBRelatedInit(); // Preparations for further USB initialization
	InitUSB();			// Initialize USB
	InitTicks();		// Initialize Timer

	//InitLED();        // Initialize LED 
	//LEDBlink();		// Perhaps we'll flash the LED
	
	while (TRUE)
	{
		HandleUSBEvents(); // Handling USB events

		if (WaitTick++ >= KEY_DELAY)
		{
			if (WaitCounter < KEY_DELAY)
			{
				WaitCounter++;
			}
		}
		
		if (SendKeysEnabled && WaitCounter >= KEY_DELAY)
		{
			if (KeyPayload[KeyIndex])
			{
				SendKey(0x00, 0x00);
				SendKey(0x00, 0x00);
				SendKey(0x00, 0x00);
				SendKey(0x00, 0x00);
				SendKey(KeyPayload[KeyIndex], KeyPayload[KeyIndex + 1]);
				SendKey(0x00, 0x00);
			}
			else
			{
				WaitCounter = 0;
				WaitTick = 0;
			}

			//Move to next key
			KeyIndex += 2;
			
			//Are we done?
			if (KeyIndex >= sizeof(KeyPayload))
			{
				SendKeysEnabled = 0x00;
			}
		}
	}
}
